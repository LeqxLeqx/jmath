package jmath.graph;

/**
 * An exception to be thrown by
 * a failure to properly propagate
 * over a graph
 */
public class PropagationException extends Exception {

  PropagationException(String message) {
    super(message);
  }

  PropagationException(Throwable e, String message) {
    super(message, e);
  }

}
